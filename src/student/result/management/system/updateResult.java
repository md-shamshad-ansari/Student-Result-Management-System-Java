    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String physics = jTextField2.getText();
        String chemisty = jTextField3.getText();
        String mathematics = jTextField4.getText();
        String dbms = jTextField5.getText();
        String humanities = jTextField6.getText();
        if(physics.length()==0 || chemisty.length()==0 || mathematics.length()==0 || dbms.length()==0 || humanities.length()==0)
            JOptionPane.showMessageDialog(null, "Insert marks of all Subject");
        else{
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root","786@92MySQL");
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select * from student where Roll_No='"+rollno+"'");
                if(rs.next()){
                    st.executeUpdate("UPDATE result SET Physics = '"+physics+"', Chemistry = '"+chemisty+"', Mathematics = '"+mathematics+"', DBMS = '"+dbms+"', Humanities = '"+humanities+"' WHERE Roll_No = '"+rollno+"'");
                    JOptionPane.showMessageDialog(null, "Successfully Updated");
                    setVisible(false);
                    new insertNewResult().setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Roll number does not exist");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.toString());
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

