    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course = (String) jComboBox1.getSelectedItem();
        String branch = (String) jComboBox2.getSelectedItem();
        String rollno = jTextField1.getText();
        String name = jTextField2.getText();
        String gender = (String) jComboBox3.getSelectedItem();
        String fathername = jTextField3.getText();
        if(rollno.length()==0 || name.length()==0 || fathername.length()==0)
           JOptionPane.showMessageDialog(null, "Enter all the details Correctly");
        else{
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root","786@92MySQL");
                Statement st = con.createStatement();
                st.executeUpdate("insert into student values('"+rollno+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+fathername+"')");
                JOptionPane.showMessageDialog(null, "Successfully Added");
                setVisible(false);
                new adminHome().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.toString());
            }
        }
        
                
    }//GEN-LAST:event_jButton6ActionPerformed

